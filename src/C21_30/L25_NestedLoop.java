package C21_30;

public class L25_NestedLoop {
    public static void main(String[] args){

        //nested loop = A loop inside another loop

        for (int i = 0; i < 10; i++) {
            for (int j = 0; j < 10; j++) {
                System.out.print(j+"  ");
            }
            System.out.println();

        }


    }
}
